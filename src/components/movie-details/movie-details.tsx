import * as React from 'react';
import {Movie} from '../../types';

interface Props {
  movie: Movie;
}

const MovieDetails: React.FunctionComponent<Props> = (props: Props) => {
  const {movie} = props;
  const hours: number = Math.floor(movie.runTime / 60);
  const minutes: number = Math.floor(movie.runTime % 60);
  return (
    <div className="movie-card__text movie-card__row">
      <div className="movie-card__text-col">
        <p className="movie-card__details-item">
          <strong className="movie-card__details-name">Director</strong>
          <span className="movie-card__details-value">{movie.director}</span>
        </p>
        <p className="movie-card__details-item">
          <strong className="movie-card__details-name">Starring</strong>
          <span className="movie-card__details-value">
            {movie.starring.map((it) => {
              return <React.Fragment key={it}>
                {it},
                <br />
              </React.Fragment>;
            })}
          </span>
        </p>
      </div>

      <div className="movie-card__text-col">
        <p className="movie-card__details-item">
          <strong className="movie-card__details-name">Run Time</strong>
          <span className="movie-card__details-value">{hours}h {minutes}m</span>
        </p>
        <p className="movie-card__details-item">
          <strong className="movie-card__details-name">Genre</strong>
          <span className="movie-card__details-value">{movie.genre}</span>
        </p>
        <p className="movie-card__details-item">
          <strong className="movie-card__details-name">Released</strong>
          <span className="movie-card__details-value">{movie.released}</span>
        </p>
      </div>
    </div>
  );
};

export default MovieDetails;
